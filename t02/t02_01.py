"""
Записати умови, що істинні тоді й тільки тоді, коли:
a) натуральне число 𝑛 – парне;
b) остання цифра числа 𝑛 – 0;
c) сума першої і другої цифри двозначного натурального числа 𝑛
– двозначне число.
"""

n = int(input())

if n % 2 == 0:
    print(n, "- парне")

if n % 10 == 0:
    print(n, "- остання цифра - 0")

if 9 < n < 100:
    d1 = n // 10
    d2 = n % 10
    s = d1 + d2
    if 9 < s < 100:
        print(n, "- задовольняє умову c)")
